<?php
  class Operaciones
  {
      public function Suma($a,$b)
      {
          return ($a + $b);
      }
      public function Resta($a,$b)
      {
          return ($a - $b);
      }
      public function Multi($a,$b)
      {
          return ($a * $b);
      }
      public function Divi($a,$b)
      {
          return ($a / $b);
      }
    }
       ?>

